#!/usr/bin/env bash
# 纯显式命令版（无变量/无参数）：
# 直接执行 12 电机固定流程（前腿 can0，后腿 can1）
# 顺序：
# 1) zero -> 2) enable -> 3) 上报频率(0x7026=39, 约5Hz)
# 4) 主动上报开 -> 5) MIT 默认 -> 6) 主动上报关 -> 7) reset

set -e

echo "[1/7] ZERO: 设置零位"
# 前腿 can0: 11 12 13 21 22 23
cansend can0 0600FD0B#0100000000000000   # ID11
cansend can0 0600FD0C#0100000000000000   # ID12
cansend can0 0600FD0D#0100000000000000   # ID13
cansend can0 0600FD15#0100000000000000   # ID21
cansend can0 0600FD16#0100000000000000   # ID22
cansend can0 0600FD17#0100000000000000   # ID23
# 后腿 can1: 51 52 53 61 62 63
cansend can1 0600FD33#0100000000000000   # ID51
cansend can1 0600FD34#0100000000000000   # ID52
cansend can1 0600FD35#0100000000000000   # ID53
cansend can1 0600FD3D#0100000000000000   # ID61
cansend can1 0600FD3E#0100000000000000   # ID62
cansend can1 0600FD3F#0100000000000000   # ID63

echo "[2/7] ENABLE: 电机使能"
cansend can0 0300FD0B#0000000000000000   # ID11
cansend can0 0300FD0C#0000000000000000   # ID12
cansend can0 0300FD0D#0000000000000000   # ID13
cansend can0 0300FD15#0000000000000000   # ID21
cansend can0 0300FD16#0000000000000000   # ID22
cansend can0 0300FD17#0000000000000000   # ID23
cansend can1 0300FD33#0000000000000000   # ID51
cansend can1 0300FD34#0000000000000000   # ID52
cansend can1 0300FD35#0000000000000000   # ID53
cansend can1 0300FD3D#0000000000000000   # ID61
cansend can1 0300FD3E#0000000000000000   # ID62
cansend can1 0300FD3F#0000000000000000   # ID63

echo "[3/7] SET REPORT RATE: 写参数 0x7026=39 (约 5Hz)"
# CMD=0x12, data=26 70 00 00 27 00 00 00
cansend can0 1200FD0B#2670000027000000   # ID11
cansend can0 1200FD0C#2670000027000000   # ID12
cansend can0 1200FD0D#2670000027000000   # ID13
cansend can0 1200FD15#2670000027000000   # ID21
cansend can0 1200FD16#2670000027000000   # ID22
cansend can0 1200FD17#2670000027000000   # ID23
cansend can1 1200FD33#2670000027000000   # ID51
cansend can1 1200FD34#2670000027000000   # ID52
cansend can1 1200FD35#2670000027000000   # ID53
cansend can1 1200FD3D#2670000027000000   # ID61
cansend can1 1200FD3E#2670000027000000   # ID62
cansend can1 1200FD3F#2670000027000000   # ID63

echo "[4/7] REPORT ON: 主动上报开启"
cansend can0 1800FD0B#0102030405060100   # ID11
cansend can0 1800FD0C#0102030405060100   # ID12
cansend can0 1800FD0D#0102030405060100   # ID13
cansend can0 1800FD15#0102030405060100   # ID21
cansend can0 1800FD16#0102030405060100   # ID22
cansend can0 1800FD17#0102030405060100   # ID23
cansend can1 1800FD33#0102030405060100   # ID51
cansend can1 1800FD34#0102030405060100   # ID52
cansend can1 1800FD35#0102030405060100   # ID53
cansend can1 1800FD3D#0102030405060100   # ID61
cansend can1 1800FD3E#0102030405060100   # ID62
cansend can1 1800FD3F#0102030405060100   # ID63

echo "[5/7] MIT DEFAULT: p=0 v=0 kp=20 kd=1.5 tau=0"
# tau=0 -> CAN ID 前缀 0x018000xx
# data=800080000A3D4CCD
cansend can0 0180000B#800080000A3D4CCD   # ID11
cansend can0 0180000C#800080000A3D4CCD   # ID12
cansend can0 0180000D#800080000A3D4CCD   # ID13
cansend can0 01800015#800080000A3D4CCD   # ID21
cansend can0 01800016#800080000A3D4CCD   # ID22
cansend can0 01800017#800080000A3D4CCD   # ID23
cansend can1 01800033#800080000A3D4CCD   # ID51
cansend can1 01800034#800080000A3D4CCD   # ID52
cansend can1 01800035#800080000A3D4CCD   # ID53
cansend can1 0180003D#800080000A3D4CCD   # ID61
cansend can1 0180003E#800080000A3D4CCD   # ID62
cansend can1 0180003F#800080000A3D4CCD   # ID63

echo "[6/7] REPORT OFF: 主动上报关闭"
cansend can0 1800FD0B#0102030405060000   # ID11
cansend can0 1800FD0C#0102030405060000   # ID12
cansend can0 1800FD0D#0102030405060000   # ID13
cansend can0 1800FD15#0102030405060000   # ID21
cansend can0 1800FD16#0102030405060000   # ID22
cansend can0 1800FD17#0102030405060000   # ID23
cansend can1 1800FD33#0102030405060000   # ID51
cansend can1 1800FD34#0102030405060000   # ID52
cansend can1 1800FD35#0102030405060000   # ID53
cansend can1 1800FD3D#0102030405060000   # ID61
cansend can1 1800FD3E#0102030405060000   # ID62
cansend can1 1800FD3F#0102030405060000   # ID63

echo "[7/7] RESET: 电机失能/停止"
cansend can0 0400FD0B#0000000000000000   # ID11
cansend can0 0400FD0C#0000000000000000   # ID12
cansend can0 0400FD0D#0000000000000000   # ID13
cansend can0 0400FD15#0000000000000000   # ID21
cansend can0 0400FD16#0000000000000000   # ID22
cansend can0 0400FD17#0000000000000000   # ID23
cansend can1 0400FD33#0000000000000000   # ID51
cansend can1 0400FD34#0000000000000000   # ID52
cansend can1 0400FD35#0000000000000000   # ID53
cansend can1 0400FD3D#0000000000000000   # ID61
cansend can1 0400FD3E#0000000000000000   # ID62
cansend can1 0400FD3F#0000000000000000   # ID63

echo "done."
